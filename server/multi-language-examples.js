const getLanguageExample = (language, type = 'cbc') => {
  const langExamples = multiLanguageExamples[language];
  if (!langExamples) return null;
  
  if (type === 'cbc') {
    return langExamples.cbcExample;
  }
  
  return langExamples.commonQuestions || {};
};

const getTranslatedResponse = (language, questionKey) => {
  const langExamples = multiLanguageExamples[language];
  if (!langExamples || !langExamples.commonQuestions) return null;
  
  return langExamples.commonQuestions[questionKey];
};

